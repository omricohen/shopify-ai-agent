function generateCustom(
  title: string,
  description: string,
  sections: string[],
  style: StyleType
): string {
  let inner = heroSection(title, description);

  for (const section of sections) {
    const s = section.toLowerCase();
    if (s === "hero") continue; // already included
    if (s === "features") {
      inner += "\n\n" + featuresSection([
        { title: "Feature One", desc: "Description of your first feature." },
        { title: "Feature Two", desc: "Description of your second feature." },
        { title: "Feature Three", desc: "Description of your third feature." },
      ]);
    } else if (s === "testimonials") {
      inner += `\n\n  <section class="section">
    <h2 style="text-align:center;margin-bottom:2rem;">Testimonials</h2>
    <div class="grid-2">
      <div class="card"><p style="font-style:italic;">"Amazing experience!"</p><strong>— Customer A</strong></div>
      <div class="card"><p style="font-style:italic;">"Highly recommend."</p><strong>— Customer B</strong></div>
    </div>
  </section>`;
    } else if (s === "cta") {
      inner += "\n\n" + ctaSection("Take the next step", "Get Started");
    } else {
      inner += `\n\n  <section class="section">
    <h2>${section}</h2>
    <p>Content for the "${section}" section. Customize this in the theme editor.</p>
  </section>`;
    }
  }

  if (sections.length === 0) {
    inner += "\n\n" + featuresSection([
      { title: "Feature One", desc: "Describe your first feature." },
      { title: "Feature Two", desc: "Describe your second feature." },
      { title: "Feature Three", desc: "Describe your third feature." },
    ]);
    inner += "\n\n" + ctaSection("Get started today", "Shop Now");
  }

  return wrapPage(title, style, inner);
}

export function generateLiquidTemplate(options: LiquidTemplateOptions): string {
  const style = (options.style as StyleType) || "modern";
  const sections = options.sections || [];
  const pageType = options.page_type as PageType;

  switch (pageType) {
    case "landing":
      return generateLanding(options.title, options.description, sections, style);
    case "about":
      return generateAbout(options.title, options.description, sections, style);
    case "contact":
      return generateContact(options.title, options.description, sections, style);
    case "faq":
      return generateFaq(options.title, options.description, sections, style);
    case "product-showcase":
      return generateProductShowcase(options.title, options.description, sections, style);
    case "custom":
    default:
      return generateCustom(options.title, options.description, sections, style);
  }
}
