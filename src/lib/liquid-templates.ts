function heroSection(title: string, subtitle: string): string {
  return `  <section class="hero section">
    <h1>{{ section.settings.heading | default: "${title}" }}</h1>
    <p>${subtitle}</p>
    <a href="#content" class="btn-primary">Learn More</a>
  </section>`;
}

function featuresSection(features: { title: string; desc: string }[]): string {
  const cards = features
    .map(
      (f) => `      <div class="card">
        <h3 style="margin-bottom:0.5rem;font-weight:var(--fw);">${f.title}</h3>
        <p>${f.desc}</p>
      </div>`
    )
    .join("\n");
  return `  <section class="section" id="content">
    <h2 style="text-align:center;margin-bottom:2rem;">Features</h2>
    <div class="grid-3">
${cards}
    </div>
  </section>`;
}

function ctaSection(text: string, buttonLabel: string): string {
  return `  <section class="section" style="text-align:center;background:linear-gradient(135deg,var(--accent),#8b5cf6);border-radius:var(--radius);padding:3rem;color:#fff;margin-top:2rem;">
    <h2 style="color:#fff;">${text}</h2>
    <a href="/collections/all" class="btn-primary" style="background:#fff;color:var(--accent);margin-top:1rem;">${buttonLabel}</a>
  </section>`;
}

function generateLanding(
  title: string,
  description: string,
  sections: string[],
  style: StyleType
): string {
  let inner = heroSection(title, description);

  if (sections.includes("features") || sections.length === 0) {
    inner += "\n\n" + featuresSection([
      { title: "Fast Shipping", desc: "Free 2-day shipping on all orders over $50." },
      { title: "Quality Guaranteed", desc: "Every product is inspected for the highest standards." },
      { title: "24/7 Support", desc: "Our team is always here to help you." },
    ]);
  }

  if (sections.includes("testimonials") || sections.length === 0) {
    inner += `\n\n  <section class="section">
    <h2 style="text-align:center;margin-bottom:2rem;">What Our Customers Say</h2>
    <div class="grid-2">
      <div class="card">
        <p style="font-style:italic;">"Absolutely love the quality and the fast delivery!"</p>
        <strong>— Happy Customer</strong>
      </div>
      <div class="card">
        <p style="font-style:italic;">"Best shopping experience I've had online."</p>
        <strong>— Loyal Shopper</strong>
      </div>
    </div>
  </section>`;
  }

  if (sections.includes("cta") || sections.length === 0) {
    inner += "\n\n" + ctaSection("Ready to get started?", "Shop Now");
  }

  return wrapPage(title, style, inner, [
    { type: "text", id: "subtitle", label: "Subtitle", default: description },
  ]);
}

