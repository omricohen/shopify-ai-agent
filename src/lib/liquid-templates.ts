function generateFaq(
  title: string,
  description: string,
  _sections: string[],
  style: StyleType
): string {
  const faqs = [
    { q: "What is your return policy?", a: "We offer a 30-day money-back guarantee on all products. Simply contact our support team to initiate a return." },
    { q: "How long does shipping take?", a: "Standard shipping takes 3-5 business days. Express shipping is available for 1-2 business day delivery." },
    { q: "Do you ship internationally?", a: "Yes! We ship to over 50 countries worldwide. International shipping typically takes 7-14 business days." },
    { q: "How can I track my order?", a: "Once your order ships, you'll receive a tracking number via email. You can also check your order status in your account." },
    { q: "Can I change or cancel my order?", a: "Orders can be modified or cancelled within 1 hour of placement. After that, please contact our support team." },
  ];

  const faqHtml = faqs
    .map(
      (f) => `    <details style="border:1px solid #e5e7eb;border-radius:var(--radius);padding:1.25rem;margin-bottom:0.75rem;">
      <summary style="cursor:pointer;font-weight:var(--fw);list-style:none;display:flex;justify-content:space-between;align-items:center;">
        ${f.q}
        <span style="font-size:1.25rem;transition:transform 0.2s;">+</span>
      </summary>
      <p style="margin-top:1rem;">${f.a}</p>
    </details>`
    )
    .join("\n");

  const inner = `  <section class="hero section">
    <h1>{{ section.settings.heading | default: "${title}" }}</h1>
    <p>${description}</p>
  </section>

  <section class="section" style="max-width:800px;margin:0 auto;">
${faqHtml}
  </section>

${ctaSection("Still have questions?", "Contact Us")}`;

  return wrapPage(title, style, inner);
}

